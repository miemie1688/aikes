convertStaticPathToAssets(path) {
  if (!path) return '';
  if (!path.startsWith('static/img/')) return path;

  const filenameWithHash = path.replace('static/img/', '');
  // ÂéªÊéâÂìàÂ∏å
  const filename = filenameWithHash.replace(/\.[0-9a-f]{6,}\.(\w+)$/, '.$1');
  return `@/assets/${filename}`;
}

